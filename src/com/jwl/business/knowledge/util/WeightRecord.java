package com.jwl.business.knowledge.util;

public class WeightRecord {
	private String name;
	private float weight;
	
	public WeightRecord(String name, float weight) {
		this.name = name;
		this.weight = weight;
	}
	public String getName() {
		return name;
	}
	public float getWeight() {
		return weight;
	}
}
